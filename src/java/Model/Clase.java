package Model;

/**
 *
 * @author Demis
 */
public class Clase {
    private int id;
    private String nombre;
    private int id_grupo;

    public Clase(int id, String nombre, int id_grupo) {
        this.id = id;
        this.nombre = nombre;
        this.id_grupo = id_grupo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getId_grupo() {
        return id_grupo;
    }

    public void setId_grupo(int id_grupo) {
        this.id_grupo = id_grupo;
    }
    
    
}
